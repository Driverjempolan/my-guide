function greet() {
	// body of the function
	console.log('hello world');
}
greet();

// with param
function greet1(name) { // name here is parameter
    // body of the function
    console.log('hello '+name);
}
greet1('john'); // john here is argument

// with multiple parameters 
function greet2(firstName, lastName) { // name here is parameter
    // body of the function
    console.log('hello ' + firstName+' '+lastName);
}
greet2('john','smith'); // john here is argument

